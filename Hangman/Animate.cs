        public static int incorrectGuesses = 0;
        const int leftOffset = 4;
        public static char[] progress = new char[Words.currentWord.Length];

        public static void FillProgress()
        {
            for (int progressIndex = 0; progressIndex < Words.currentWord.Length; ++progressIndex)
            {
                progress[progressIndex] = '_';
            }
        }
        public static void RenderGameState()
        {
            Console.SetCursorPosition(0, 2);
            Console.Write(limbs[incorrectGuesses]);
            Console.Write("\n\n");
            Console.CursorLeft = leftOffset;
            foreach (char c in progress)
            {
                Console.Write(c + " ");
            }
            Console.Write("     ");
        }

        public static void Death()
        {
            Console.CursorVisible = false;
            for (int frameIndex = 0; frameIndex < death.Length; ++frameIndex)
            {
                Console.SetCursorPosition(0, 2);
                Console.Write(death[frameIndex]);
                Thread.Sleep(TimeSpan.FromMilliseconds(200));
            }
            Console.Write("\n\n");
            Console.CursorLeft = leftOffset;
            foreach (char c in Words.currentWord)
            {
                Console.Write(c + " ");
            }
            Console.CursorVisible = false;
            Console.Write("\n\n    вы проиграли :^((((");
        }

        public static void Win()
        {
            RenderGameState();
            Console.CursorVisible = false;
            for (int frameIndex = incorrectGuesses; frameIndex < limbs.Length; ++frameIndex)
            {
                Console.SetCursorPosition(0, 2);
                Console.Write(limbs[frameIndex]);
                Thread.Sleep(TimeSpan.FromMilliseconds(200));
            }
            for (int frameIndex = 0; frameIndex < win.Length; ++frameIndex)
            {
                Console.SetCursorPosition(0, 2);
                Console.Write(win[frameIndex]);
                Thread.Sleep(TimeSpan.FromMilliseconds(200));
            }
            Console.Write("\n\n\n\n    ура!!! победа");
        }

